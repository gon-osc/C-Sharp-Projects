using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Drill219___ASSIGNMENT
{
    class MathOperation
    {
        public int mult(int x, int y)
        {
            int a = x;
            int b = y;
            int result = a * b;

            return result;
        }
        public double Sub(double m, double n)
        {
            double c = m;
            double d = n;
            double result = c - d;

            return result;
        }
        public int Add(int w, int z)
        {
            int num1 = w;
            int num2 = z;
            int add = num1 + num2;

            return add;
        }
    }
}
